public class BitwiseAndOperator {
    public static void main(String args[]) {
       int a = 6;
       int b = 7;
  
       // Binary representation of 6 is 0110
       // Binary representation of 7 is 0111
       // Result is                     0110 = 6
       System.out.println("a & b = " + (a & b));
   }
 }
 //1 & 1 = 1

 //1 & 0 = 0 